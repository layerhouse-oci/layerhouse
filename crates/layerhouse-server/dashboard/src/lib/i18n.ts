function isLocale(value: string | null | undefined): value is Locale {
  return !!value && (LOCALES as readonly string[]).includes(value);
}

function browserLocale(): Locale {
  if (typeof navigator === "undefined") return "en";
  const primary = navigator.language?.split("-")[0];
  return isLocale(primary) ? primary : "en";
}

function initialLocale(): Locale {
  if (typeof localStorage === "undefined") return browserLocale();
  const stored = localStorage.getItem(STORAGE_KEY);
  return isLocale(stored) ? stored : browserLocale();
}

const [localeSignal, setLocaleSignal] = createSignal<Locale>(initialLocale());

export const locale = localeSignal;

export function setLocale(next: Locale) {
  setLocaleSignal(next);
  try {
    localStorage.setItem(STORAGE_KEY, next);
  } catch {
    // Ignore storage failures in private or locked-down browser contexts.
  }
}

export function localeDir(next = locale()): "ltr" | "rtl" {
  return next === "ar" ? "rtl" : "ltr";
}

export function syncLocaleDocument() {
  if (typeof document === "undefined") return;
  const current = locale();
  document.documentElement.lang = current;
  document.documentElement.dir = localeDir(current);
  document.title = t("app.productName");
}

export function t(key: string, params: Record<string, string | number> = {}): string {
  const current = locale();
  const raw = commonOverrides[current]?.[key] ?? en[key] ?? key;
  return raw.replace(/\{(\w+)\}/g, (_, name) => String(params[name] ?? ""));
}
